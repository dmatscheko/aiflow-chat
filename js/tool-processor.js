/**
 * @fileoverview Centralized logic for processing tool calls.
 * This file introduces the ToolCallManager, which orchestrates the entire
 * lifecycle of tool usage, from parsing to sequential execution and response handling.
 */

'use strict';

import { pluginManager } from './plugin-manager.js';

/**
 * @typedef {import('./main.js').App} App
 * @typedef {import('./chat-data.js').Message} Message
 * @typedef {import('./chat-data.js').ChatLog} ChatLog
 * @typedef {import('./plugins/chats-plugin.js').Chat} Chat
 */

/**
 * @typedef {object} ToolCall
 * @property {string} tool_call_id - A unique identifier for the tool call, generated by the system.
 * @property {string} name - The name of the tool being called.
 * @property {object} params - The parameters for the tool call.
 */

/**
 * @typedef {object} ToolResult
 * @property {string} tool_call_id - The ID of the tool call this is a result for.
 * @property {string} name - The name of the tool that was called.
 * @property {string | null} content - The stringified result of the tool execution.
 * @property {string | null} error - A string describing an error, if one occurred.
 * @property {boolean} [isStreaming=false] - Indicates if the tool provides a streaming response.
 */

/**
 * @callback ExecuteToolCallback
 * @param {ToolCall} call - The tool call to execute.
 * @param {ToolCallJob} job - The job this call belongs to.
 * @returns {Promise<void>} A promise that resolves when the tool execution is complete.
 * The implementation MUST call toolCallManager.notifyCallComplete() with the result.
 */

/**
 * @typedef {object} ToolExecutor
 * @property {(call: ToolCall) => boolean} canExecute - A function that returns true if this executor can handle the given call.
 * @property {ExecuteToolCallback} execute - The function that performs the tool call.
 */

/**
 * Represents a single job, which consists of all tool calls from one assistant message.
 * The calls within a job are processed sequentially.
 * @class
 */
class ToolCallJob {
    /**
     * @param {object} options
     * @param {string} options.id - A unique ID for the job.
     * @param {Message} options.originalMessage - The assistant message that triggered the job.
     * @param {ChatLog} options.chatLog - The chat log instance to add messages to.
     * @param {ToolCall[]} options.calls - The array of tool calls to execute.
     */
    constructor({ id, originalMessage, chatLog, calls }) {
        /** @type {string} */
        this.id = id;
        /** @type {Message} */
        this.originalMessage = originalMessage;
        /** @type {ChatLog} */
        this.chatLog = chatLog;
        /** @type {ToolCall[]} */
        this.calls = calls;
        /** @type {number} */
        this.nextCallIndex = 0;
        /** @type {string} */
        this.lastMessageId = originalMessage.id;
        /** @type {string | null} */
        this.callingAgentId = originalMessage.value.agent || null;
    }

    /** @returns {boolean} */
    isComplete() {
        return this.nextCallIndex >= this.calls.length;
    }

    /** @returns {ToolCall | null} */
    getNextCall() {
        return this.isComplete() ? null : this.calls[this.nextCallIndex];
    }
}

/**
 * Manages the entire lifecycle of tool calls, including queuing,
 * sequential execution, and handling nested calls.
 * @class
 */
class ToolCallManager {
    constructor() {
        /**
         * The stack of jobs to be processed. Last in, first out.
         * @private
         * @type {ToolCallJob[]}
         */
        this.jobStack = [];
        /**
         * A flag to prevent the processing loop from running multiple times concurrently.
         * @private
         * @type {boolean}
         */
        this.isProcessing = false;
        /**
         * A map of registered tool executors.
         * @private
         * @type {Map<string, ToolExecutor>}
         */
        this.toolExecutors = new Map();
        /**
         * A counter to ensure tool call ID uniqueness.
         * @private
         * @type {number}
         */
        this.uniqueCounter = 0;

        /** @type {App | null} */
        this.app = null;
    }

    /**
     * @param {App} app
     */
    init(app) {
        this.app = app;
    }

    /**
     * Registers a plugin capable of executing certain types of tool calls.
     * @param {string} name - The name of the executor (e.g., 'mcp', 'agent-caller').
     * @param {ToolExecutor} executor - The executor object.
     */
    registerExecutor(name, executor) {
        this.toolExecutors.set(name, executor);
    }

    /**
     * Creates and adds a new job to the processing stack.
     * This is the main entry point for starting a tool call workflow.
     * It parses the message, assigns unique IDs, updates the message content,
     * and kicks off the processing loop.
     * @param {Message} assistantMessage - The message from the assistant containing tool calls.
     * @param {Chat} chat - The active chat instance.
     * @returns {boolean} - True if a job was created, false otherwise.
     */
    addJob(assistantMessage, chat) {
        const { toolCalls, updatedContent } = this._parseAndPrepareCalls(assistantMessage.value.content);

        if (toolCalls.length === 0) {
            return false;
        }

        // Update the original message to include the system-generated, unique tool_call_ids
        assistantMessage.value.content = updatedContent;
        // Invalidate the cache to force a re-render of the message with the new IDs
        assistantMessage.cache = null;
        chat.log.notify();

        const newJob = new ToolCallJob({
            id: `job_${Date.now()}`,
            originalMessage: assistantMessage,
            chatLog: chat.log,
            calls: toolCalls,
        });

        this.jobStack.push(newJob);
        console.log(`ToolCallManager: Added new job ${newJob.id} with ${newJob.calls.length} call(s). Stack size: ${this.jobStack.length}`);
        this.processLoop();
        return true;
    }

    /**
     * The main processing loop. It runs as long as there are jobs on the stack.
     * It processes jobs in a LIFO (Last-In, First-Out) manner.
     * @private
     */
    async processLoop() {
        if (this.isProcessing) return;
        this.isProcessing = true;
        console.log('ToolCallManager: Starting process loop.');

        while (this.jobStack.length > 0) {
            const currentJob = this.jobStack[this.jobStack.length - 1];

            if (currentJob.isComplete()) {
                this._finishJob(currentJob);
                continue; // Loop to process the next job on the stack (if any)
            }

            const callToDispatch = currentJob.getNextCall();
            if (callToDispatch) {
                // Dispatching the call will suspend the loop.
                // The loop will be re-entered via notifyCallComplete.
                await this._dispatchCall(callToDispatch, currentJob);
                // We break here because dispatch is async and the state might have changed.
                // notifyCallComplete will re-trigger the loop.
                break;
            }
        }

        console.log('ToolCallManager: Process loop finished for now.');
        this.isProcessing = false;
    }

    /**
     * Called by an executor when a tool call is complete.
     * This method adds the tool response to the chat and continues the loop.
     * @param {string} jobId - The ID of the job the call belonged to.
     * @param {ToolResult} result - The result of the tool execution.
     */
    notifyCallComplete(jobId, result) {
        console.log(`ToolCallManager: Received completion notification for call ${result.tool_call_id} in job ${jobId}`);
        const job = this.jobStack.find(j => j.id === jobId);
        if (!job) {
            console.error(`ToolCallManager: Cannot find job with ID ${jobId}.`);
            return;
        }

        // Create the tool response message content
        const innerContent = result.error
            ? `<error>\n${result.error}\n</error>`
            : `<content>\n${result.content || ''}\n</content>`;
        const toolResponseMessageContent = `<dma:tool_response name="${result.name}" tool_call_id="${result.tool_call_id}">\n${innerContent}\n</dma:tool_response>`;

        // Add the tool response message, chaining it to the last message
        const newToolMessage = job.chatLog.addMessage({
            role: 'tool',
            content: toolResponseMessageContent,
            tool_call_id: result.tool_call_id,
            name: result.name,
        }, job.lastMessageId);

        // Update the job state
        job.lastMessageId = newToolMessage.id;
        job.nextCallIndex++;

        // Continue processing
        this.processLoop();
    }

    /**
     * Finds the appropriate executor and tells it to run the tool call.
     * @param {ToolCall} call - The tool call to execute.
     * @param {ToolCallJob} job - The job this call belongs to.
     * @private
     */
    async _dispatchCall(call, job) {
        console.log(`ToolCallManager: Dispatching call ${call.tool_call_id} (${call.name})`);
        for (const executor of this.toolExecutors.values()) {
            if (executor.canExecute(call)) {
                // The execution is intentionally not awaited here in the traditional sense.
                // The executor is responsible for calling `notifyCallComplete` which resumes the flow.
                executor.execute(call, job);
                return;
            }
        }
        console.error(`ToolCallManager: No executor found for tool call "${call.name}".`);
        // If no executor is found, we must still "complete" the call to avoid getting stuck.
        this.notifyCallComplete(job.id, {
            tool_call_id: call.tool_call_id,
            name: call.name,
            error: `No executor found for tool "${call.name}".`,
            content: null,
        });
    }

    /**
     * Finalizes a job by adding a pending assistant message and popping it from the stack.
     * @param {ToolCallJob} job - The job to finish.
     * @private
     */
    _finishJob(job) {
        console.log(`ToolCallManager: Finishing job ${job.id}.`);
        // After all tool results, queue up the next step for the AI.
        job.chatLog.addMessage({
            role: 'assistant',
            content: null,
            agent: job.callingAgentId,
        }, job.lastMessageId);

        // Pop the completed job from the stack
        this.jobStack.pop();

        // Immediately trigger the response processor for the new pending message
        if (this.app) {
            this.app.responseProcessor.scheduleProcessing(this.app);
        }
    }

    /**
     * Parses tool calls from content, assigns new unique IDs, and rebuilds the content.
     * @param {string | null} content - The message content to parse.
     * @returns {{toolCalls: ToolCall[], updatedContent: string}}
     * @private
     */
    _parseAndPrepareCalls(content) {
        const toolCalls = [];
        if (!content) return { toolCalls, updatedContent: '' };

        const functionCallRegex = /(<dma:tool_call\s+[^>]+?\/?>)/gi;
        let lastIndex = 0;
        const newContentParts = [];

        for (const match of content.matchAll(functionCallRegex)) {
            const fullMatch = match[0];
            const startIndex = match.index;

            // Add the text before this match
            newContentParts.push(content.slice(lastIndex, startIndex));

            let attributes = fullMatch.slice(15, -1).trim();
            if (attributes.endsWith('/')) {
                attributes = attributes.slice(0, -1).trim();
            }

            const nameMatch = /name="([^"]*)"/.exec(attributes);
            if (!nameMatch) {
                newContentParts.push(fullMatch); // Not a valid call, push as is
                lastIndex = startIndex + fullMatch.length;
                continue;
            }
            const name = nameMatch[1];
            const newId = `call_${Date.now()}_${this.uniqueCounter++}`;

            // Create the tool call object
            const call = { tool_call_id: newId, name, params: {} };

            // This is a simplified parser. The full param parsing will happen in the executor
            // if needed, but for now, we just need to inject the ID.
            toolCalls.push(call);

            // Rebuild the tag with the new ID, removing any old one
            let newTag = fullMatch.replace(/\s+tool_call_id\s*=\s*["'][^"']*["']/g, '');
            const insert = ` tool_call_id="${newId}"`;
            const endSlice = newTag.endsWith('/>') ? -2 : -1;
            newTag = newTag.slice(0, endSlice) + insert + newTag.slice(endSlice);

            newContentParts.push(newTag);
            lastIndex = startIndex + fullMatch.length;
        }

        // Add the remaining part of the string
        newContentParts.push(content.slice(lastIndex));

        return {
            toolCalls,
            updatedContent: newContentParts.join(''),
        };
    }
}

// --- Tool Call Parsing (Legacy, for reference or simple parsing needs) ---

/**
 * Parses tool calls from an assistant's message content without creating a job.
 * Useful for checks where you only need to know if calls exist.
 * @param {string | null} content - The message content to parse.
 * @returns {ToolCall[]} The parsed tool calls.
 */
export function parseToolCalls(content) {
    const { toolCalls } = new ToolCallManager()._parseAndPrepareCalls(content);
    return toolCalls;
}

// --- Singleton Instance ---
export const toolCallManager = new ToolCallManager();